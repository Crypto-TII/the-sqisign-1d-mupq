int crypto_sign_keypair(unsigned char *pk, unsigned char *sk) {
  memcpy(pk, kat_lvl1_p248_uncompressed_pk, CRYPTO_PUBLICKEYBYTES);
  // We don't need the secret key
  memcpy(sk, 0, CRYPTO_SECRETKEYBYTES);
}

int crypto_sign(unsigned char *sm, size_t *smlen, const unsigned char *m,
                size_t mlen, const unsigned char *sk) {
  for (int i = 0; i < sizeof(kat_lvl1_p248_uncompressed) /
                          sizeof(kat_lvl1_p248_uncompressed[0]);
       i++) {
    if (mlen == kat_lvl1_p248_uncompressed[i].mlen &&
        memcmp(m, kat_lvl1_p248_uncompressed[i].msg, mlen) == 0) {
      memcpy(sm, kat_lvl1_p248_uncompressed[i].sm,
             kat_lvl1_p248_uncompressed[i].smlen);
      *smlen = kat_lvl1_p248_uncompressed[i].smlen;
      return 0;
    }
  }

  return 1;
}

int crypto_sign_open(unsigned char *m, size_t *mlen, const unsigned char *sm,
                     size_t smlen, const unsigned char *pk) {
  return sqisign_open_uncompressed(m, mlen, sm, smlen, pk);
}
