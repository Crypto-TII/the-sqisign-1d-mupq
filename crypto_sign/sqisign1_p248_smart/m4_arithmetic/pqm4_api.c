int crypto_sign_keypair(unsigned char *pk, unsigned char *sk) {
  memcpy(pk, kat_lvl1_p248_smart_pk, CRYPTO_PUBLICKEYBYTES);
  // We don't need the secret key
  memset(sk, 0, CRYPTO_SECRETKEYBYTES);
}

int crypto_sign(unsigned char *sm, size_t *smlen, const unsigned char *m,
                size_t mlen, const unsigned char *sk) {
  for (int i = 0; i < sizeof(kat_lvl1_p248_smart) / sizeof(kat_lvl1_p248_smart[0]); i++) {
    if (mlen == kat_lvl1_p248_smart[i].mlen &&
        memcmp(m, kat_lvl1_p248_smart[i].msg, mlen) == 0) {
      memcpy(sm, kat_lvl1_p248_smart[i].sm, kat_lvl1_p248_smart[i].smlen);
      *smlen = kat_lvl1_p248_smart[i].smlen;
      return 0;
    }
  }

  return 1;
}

int crypto_sign_open(unsigned char *m, size_t *mlen, const unsigned char *sm,
                     size_t smlen, const unsigned char *pk) {
  return sqisign_open_smart(m, mlen, sm, smlen, pk);
}
